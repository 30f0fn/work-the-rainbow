def date_seeded_skin():
    now = datetime.datetime.now()
    random.seed = int(f"{now.year}{now.month}{now.day}")
    return random.choice(_skins_data)

@functools.lru_cache()
def current_skin(request):
    # return _skins_data[0]
    val = request.user.skin if request.user.is_authenticated else date_seeded_skin()
    return {"current_skin" : val}

