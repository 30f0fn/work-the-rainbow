def date_seeded_skin():
    now = datetime.datetime.now()
    random.seed = int(f"{now.year}{now.month}{now.day}")
    return random.choice(_skins_data)

@functools.lru_cache()
def current_skin(request):
    # return _skins_data[0]
    str_val = request.user.skin if request.user.is_authenticated else date_seeded_skin()
    return {"current_skin" : str_to_skin(str_val)}

Skin = collections.namedtuple('Skin', 'foreground background')
def str_to_skin(s):
    print(s, s.split(','))
    try:
        foreground, background = s.split(',')
    except ValueError:
        foreground, background = date_seeded_skin().split(',')
    return Skin(foreground=foreground, background=background)

